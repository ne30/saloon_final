	function switchVisible() {
            if (document.getElementById('team-group-1')) {

                if (document.getElementById('team-group-1').style.display == 'none') {
                    document.getElementById('team-group-1').style.display = 'flex';
                    document.getElementById('team-group-2').style.display = 'none';
                    document.getElementById('team-group-3').style.display = 'none';
                    document.getElementById('team-group-4').style.display = 'none';
                    document.getElementById('team-group-5').style.display = 'none';
				    var x = document.getElementById("dropdown-content");
				    if (x.style.display === "none") {
				        x.style.display = "block";
				    } else {
				        x.style.display = "none";
				    }
                }
               
                else {
                    document.getElementById('team-group-1').style.display = 'flex';
                    document.getElementById('team-group-2').style.display = 'none';
                    document.getElementById('team-group-3').style.display = 'none';
                    document.getElementById('team-group-4').style.display = 'none';
                    document.getElementById('team-group-5').style.display = 'none';
                    				    var x = document.getElementById("dropdown-content");
				    if (x.style.display === "none") {
				        x.style.display = "block";
				    } else {
				        x.style.display = "none";
				    }

                }
            }
	}
